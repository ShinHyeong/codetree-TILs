#n=3 k=4 -> (0,2) 서 
#n=3 k=5 -> (1,2) 서 
#n=3 k=6 -> (2,2) 서 
#==> elif k<=n*2, (_,n-1) : x는 0부터 +1/y는 n-1값으로 고정

#k=7 -> (2,2) 북
#k=8 -> (2,1) 북
#k=9 -> (2,0) 북 
#==> elif k<=n*3, (n-1,_) : x는 n-1로 고정/y는 n-1부터 -1됨

#k=10 -> (2,0) 동
#k=11 -> (1,0) 동
#k=12 -> (0,0) 동 
#==> elif k<=n*4, (_,0) : x는 n-1부터 -1됨/y는 0으로 고정

